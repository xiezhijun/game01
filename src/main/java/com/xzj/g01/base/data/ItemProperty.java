package com.xzj.g01.base.data;

public class ItemProperty {
	
	private final int id;
	
	private final String name;
	
	public ItemProperty(int id, String name) {
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}
	
	
}
